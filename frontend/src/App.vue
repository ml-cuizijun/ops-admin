<!--
  ============================================================================
  App.vue - 根组件
  ============================================================================
  
  这是整个 Vue 应用的根组件（最顶层的组件）
  所有页面组件都是它的后代
  
  组件层级关系：
  ┌────────────────────────────────────────┐
  │                App.vue                 │  ← 根组件（你现在看的这个）
  │  ┌──────────────────────────────────┐  │
  │  │         <router-view>            │  │  ← 根据 URL 显示不同组件
  │  │  ┌────────────────────────────┐  │  │
  │  │  │        Layout.vue          │  │  │  ← 布局组件（侧边栏+头部）
  │  │  │  ┌──────────────────────┐  │  │  │
  │  │  │  │    <router-view>     │  │  │  │  ← 嵌套的 router-view
  │  │  │  │  ┌────────────────┐  │  │  │  │
  │  │  │  │  │  list.vue 等   │  │  │  │  │  ← 具体页面内容
  │  │  │  │  └────────────────┘  │  │  │  │
  │  │  │  └──────────────────────┘  │  │  │
  │  │  └────────────────────────────┘  │  │
  │  └──────────────────────────────────┘  │
  └────────────────────────────────────────┘
  
  为什么这里只有一个 <router-view>？
  因为这是一个典型的后台管理系统：
  - 几乎所有页面都使用同样的布局（Layout.vue）
  - Layout.vue 已经包含了侧边栏、头部等公共部分
  - 所以 App.vue 只需要一个 router-view 来显示 Layout
-->

<template>
  <!--
    router-view: Vue Router 的核心组件
    
    它的作用是：「占位符」
    - 根据当前 URL 路径，显示对应的组件
    - URL 变化时，这里显示的组件也会变化
    
    工作原理：
    1. 用户访问 http://localhost:3000/server/list
    2. Vue Router 查找 routes 配置，找到匹配的路由
    3. 发现 /server/list 对应的父组件是 Layout
    4. 在这个 <router-view> 位置渲染 Layout.vue
    5. Layout.vue 里面还有一个 <router-view>
    6. 那个 router-view 会渲染 list.vue
  -->
  <router-view />
</template>

<!--
  注意：这个组件没有 <script> 和 <style>
  
  因为它足够简单：
  - 不需要任何 JavaScript 逻辑
  - 不需要特殊样式
  
  它就像一个「空壳」，只负责告诉 Vue Router：
  「把匹配到的组件放在这里显示」
-->
